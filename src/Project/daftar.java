    protected void autonumber(){
        String no="";
        try{
            String sql="select id from login order by id asc";
            PreparedStatement stat=con.prepareStatement(sql);
            ResultSet rs=stat.executeQuery(sql);
            while(rs.next())
            {
                no=rs.getString("id");
            }
            }catch(SQLException sqle){no="";}
            
            if(no.length()<1){no="A00";}
            String ur=no.substring(2);
            int u=Integer.parseInt(ur)+1;
            if(u<10)
            {ur="00"+u;}
            else if(u<10)
            {ur="0"+u;}
            else
            {ur=""+u;}
            no="A-"+ur;
            id.setText(no);
    }
    
