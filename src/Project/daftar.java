    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        try {
            sql = "INSERT INTO `login`(`id`,`username`,`password`,`email`) VALUES (?,?,?,?)";
            java.sql.PreparedStatement stat = con.prepareStatement(sql);
            
            stat.setString(1, id.getText());
            stat.setString(2, user.getText());
            stat.setString(3, pass.getText());
            stat.setString(4, email.getText());
            
            stat.executeUpdate();
            id.requestFocus();
            data();
            bersih();
            kolom();
            autonumber();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Tambahkan");
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Gagal Disimpan");
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try {
            sql = "UPDATE login SET username=?,password=?,email=? Where id=?";
            // database ttl dan tglmsk belom di tulis karena belom berhasil di Update
            java.sql.PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, user.getText());
            stat.setString(2, pass.getText());
            stat.setString(3, email.getText());
            stat.setString(4, id.getText());
            stat.executeUpdate();
            id.requestFocus();
            JOptionPane.showMessageDialog(null, "Data Login Berhasil Di Update");
            data();
            bersih();
            kolom();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Login Gagal Di Update");
        }
    }//GEN-LAST:event_editActionPerformed

